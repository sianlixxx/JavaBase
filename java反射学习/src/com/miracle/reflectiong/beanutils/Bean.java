package com.miracle.reflectiong.beanutils;

public class Bean
{
	private int x;
	private String name;

	public Bean()
	{
		this.x = 1;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public int getX()
	{
		return x;
	}

	public void setX(int x)
	{
		this.x = x;
	}
}
